<h1 class="font-anon-bold mt-2 mb-0 text-xl text-yellow-400">The Design Of THe Shuttlecock</h1>
    <div class="font-anon hyphens-auto my-4" lang="en">
        To understand the physics behind why the shuttlecock flips during flight, such that the cork points in the direction of the motion we first need to understand the terms centre of gravity and the pressure center.
    </div>
    <div class="font-anon hyphens-auto my-4" lang="en">
        The Centre of gravity is a point from which the weight of a body or system may be considered to act. It depends upon the mass distribution over the body. As the mass is mostly concentrated in the cork, the centre of gravity is located in the cork.
    </div>
    <div class="font-anon hyphens-auto my-4" lang="en">
        The pressure centre is the point within the object where the combined effect of the air resistance forces is considered to be concentrated. It's a way to simplify the complex interaction between the object and the air into a single point. In the case of a shuttlecock, it is located near the tip of the feathers (the skirt) where the cross-sectional area is the largest.
    </div>
    <div class="font-anon hyphens-auto my-4" lang="en">
        Cg - the centre of gravity<br>
        Cp - the centre of pressure
    </div>
    <div class="font-anon hyphens-auto my-4" lang="en">
