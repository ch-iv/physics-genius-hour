    </div>
    <div class="font-anon hyphens-auto my-4" lang="en">
        Using the concepts of Cg and Cp we can explain the flipping of the shuttlecock. As the shuttlecock flies through the air, the force of drag exerted on the shuttlecock is mostly exerted at the centre of pressure, causing a negative acceleration (deceleration). We can consider the cork (where the Cg is located) and the skirt (where the Cp is located) as two separate bodies for the sake of calculating their acceleration separately.
    </div>
    <div class="font-anon hyphens-auto my-4" lang="en">
        Newton's second law states that Net force = mass acceleration . If we rearrange this equation to find the acceleration of a body we get Acceleration = Net Forcemass. So, The only force acting on the shuttlecock in the horizontal axis is aerodynamic drag. This means the net force will be equal to the drag force on the shuttlecock. As you just learnt the aerodynamic drag is experienced more at the centre of pressure. This means the net force (which will be negative as it is opposing the motion of the shuttlecock) will be greater at the centre of pressure (at the skirt) and hence the acceleration in the negative direction (deceleration) will also be greater for the skirt.
    </div>
    <div class="font-anon hyphens-auto my-4" lang="en">
        The centre of gravity is located at the cork of the shuttlecock, meaning most of the mass is concentrated there. Using newtons second law again we can see that a larger mass will result in less acceleration for a given amount of force. So overall this means that during flight a birdie will experience a much greater deceleration at its skirt as compared to the cork, this results in the skirt moving behind the cork and hence the head of the shuttlecock flips and the cork faces forwards.
    </div>
